-module(dss_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    dss_mongodb:start_mongodb(),
    {ok, _} = cowboy:start_clear(http, [{port, 80}], #{
        env => #{dispatch => dss_handler:handler()}
    }),
    dss_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

